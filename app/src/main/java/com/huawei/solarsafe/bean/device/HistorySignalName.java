public class HistorySignalName {
    private String eName;
    private String zName;
    private String enName;
    private String jaName;
    private String itName;
    private String nlName;

    public HistorySignalName(String eName, String zName,String enName,String jaName,String itName,String nlName) {
        this.zName = zName;
        this.eName = eName;
        this.enName = enName;
        this.jaName = jaName;
        this.itName = itName;
        this.nlName = nlName;
    }

    public String geteName() {
        return eName;
    }


    public String getzName() {
        return zName;
    }


    public String getEnName() {
        return enName;
    }


    public String getJaName() {
        return jaName;
    }

    public String getItName() {
        return itName;
    }

    public void setItName(String itName) {
        this.itName = itName;
    }

    public String getNlName() {
        return nlName;
    }

    public void setNlName(String nlName) {
        this.nlName = nlName;
    }

    @Override
    public String toString() {
        return "HistorySignalName{" +
                "eName='" + eName + '\'' +
                ", zName='" + zName + '\'' +
                ", enName='" + enName + '\'' +
                ", jaName='" + jaName + '\'' +
                '}';
    }
}
