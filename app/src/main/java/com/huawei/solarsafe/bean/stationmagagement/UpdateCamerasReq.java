package com.huawei.solarsafe.bean.stationmagagement;

import java.util.List;

/**
 * Created by p00229 on 2017/6/7.
 * 更新摄像头信息实体类
 */

public class UpdateCamerasReq {


    private String stationCode;

    private List<CameraInfoListBean> cameraInfoList;

    public String getStationCode() {
        return stationCode;
    }

    public void setStationCode(String stationCode) {
        this.stationCode = stationCode;
    }


    public void setCameraInfoList(List<CameraInfoListBean> cameraInfoList) {
        this.cameraInfoList = cameraInfoList;
    }

    public static class CameraInfoListBean {
        private String stationCode;
        private String name;
        private String ip;
        private String port;
        private String username;
        private String password;
        private boolean isInput;

        public void setInput(boolean input) {
            isInput = input;
        }

        public String getStationCode() {
            return stationCode;
        }

        public void setStationCode(String stationCode) {
            this.stationCode = stationCode;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public String getIp() {
            return ip;
        }

        public void setIp(String ip) {
            this.ip = ip;
        }

        public String getPort() {
            return port;
        }

        public void setPort(String port) {
            this.port = port;
        }

        public String getUsername() {
            return username;
        }

        public void setUsername(String username) {
            this.username = username;
        }

        public String getPassword() {
            return password;
        }

        public void setPassword(String password) {
            this.password = password;
        }
    }
}
