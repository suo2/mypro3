package com.huawei.solarsafe.bean.device;

import java.io.Serializable;

/**
 * Created by P00604 on 2017/11/17.
 */

public class ParamsInfo implements Serializable {
    private static final long serialVersionUID = 5785358046019716213L;
    private String simCard;
    private String endian;
    private String simPower;
    public String getSimCard() {
        return simCard;
    }

    public String getEndian() {
        return endian;
    }

    public void setEndian(String endian) {
        this.endian = endian;
    }

    @Override
    public String toString() {
        return "ParamsInfo{" +
                "simCard='" + simCard + '\'' +
                ", endian='" + endian + '\'' +
                '}';
    }

    public String getSimPower() {
        return simPower;
    }

    public void setSimPower(String simPower) {
        this.simPower = simPower;
    }
}
