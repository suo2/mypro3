public class ComparedBean {
    private String id;
    private String esnCode;
    private String pvId	;
    private String ivertName;
   private long taskId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEsnCode() {
        return esnCode;
    }

    public void setEsnCode(String esnCode) {
        this.esnCode = esnCode;
    }


    public void setPvId(String pvId) {
        this.pvId = pvId;
    }


    public void setIvertName(String ivertName) {
        this.ivertName = ivertName;
    }

    public long getTaskId() {
        return taskId;
    }

    public void setTaskId(long taskId) {
        this.taskId = taskId;
    }

    @Override
    public String toString() {
        return "ComparedBean{" +
                "id=" + id +
                ", esnCode='" + esnCode + '\'' +
                ", pvId='" + pvId + '\'' +
                ", ivertName='" + ivertName + '\'' +
                ", taskId=" + taskId +
                '}';
    }
}
