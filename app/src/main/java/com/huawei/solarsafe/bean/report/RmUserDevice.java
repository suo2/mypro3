package com.huawei.solarsafe.bean.report;

import com.huawei.solarsafe.utils.customview.treeview.TreeNodeId;
import com.huawei.solarsafe.utils.customview.treeview.TreeNodeLabel;
import com.huawei.solarsafe.utils.customview.treeview.TreeNodePid;

import java.io.Serializable;

/**
 * Created by p00507
 * on 2017/5/27.
 */
public class RmUserDevice implements Serializable {
    private static final long serialVersionUID = 7520185359316228577L;
    @TreeNodeId
    private String id = "-1";
    @TreeNodePid
    private String pid = "0";
    @TreeNodeLabel
    String name;
    String check;
    String text;
    String level;
    String sort;
    String model;
    String unit;
    boolean isChoice;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public boolean isChoice() {
        return isChoice;
    }

    public void setChoice(boolean choice) {
        isChoice = choice;
    }
}
