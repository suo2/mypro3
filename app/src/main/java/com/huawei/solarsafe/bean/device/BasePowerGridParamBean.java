public class BasePowerGridParamBean {
    public String min;
    public String max;
    public String minCalc;
    public String maxCalc;
    public boolean containsMax;//最大值是否为闭区间
    public boolean isShowInt;//是否为整数
    public boolean containsMin;
    public boolean isShow;//是否显示

    public String getMin() {
        return min;
    }

    public void setMin(String min) {
        this.min = min;
    }

    public String getMax() {
        return max;
    }

    public void setMax(String max) {
        this.max = max;
    }

    public String getMinCalc() {
        return minCalc;
    }

    public String getMaxCalc() {
        return maxCalc;
    }

    public boolean isContainsMax() {
        return containsMax;
    }

    public boolean isShowInt() {
        return isShowInt;
    }

    public boolean isContainsMin() {
        return containsMin;
    }

    public boolean isShow() {
        return isShow;
    }

    public void setShow(boolean show) {
        isShow = show;
    }
}
