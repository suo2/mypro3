public class InspectTaskDetail {

    private DetailInfo detailInfo;
    private PatrolStationBean stationInfo;

    public DetailInfo getDetailInfo() {
        return detailInfo;
    }

    public void setDetailInfo(DetailInfo detailInfo) {
        this.detailInfo = detailInfo;
    }

    public PatrolStationBean getStationInfo() {
        return stationInfo;
    }

    public void setStationInfo(PatrolStationBean stationInfo) {
        this.stationInfo = stationInfo;
    }

    public class DetailInfo {

        private long inspectId;
        private String taskId;
        private String taskState;
        private String sId;
        private String objId;
        private long completeTime;
        private int inspectResult;
        private String remark;
        private String sName;
        private int exceptionCount;
        private long currentAssignee;

        public long getInspectId() {
            return inspectId;
        }

        public void setInspectId(long inspectId) {
            this.inspectId = inspectId;
        }

        public String getTaskId() {
            return taskId;
        }

        public void setTaskId(String taskId) {
            this.taskId = taskId;
        }


        public String getSId() {
            return sId;
        }

        public void setSId(String sId) {
            this.sId = sId;
        }

        public String getObjId() {
            return objId;
        }


        public long getCompleteTime() {
            return completeTime;
        }


        public int getInspectResult() {
            return inspectResult;
        }


        public String getRemark() {
            return remark;
        }

        public void setRemark(String remark) {
            this.remark = remark;
        }

        public String getSName() {
            return sName;
        }

        public int getExceptionCount() {
            return exceptionCount;
        }

        public long getCurrentAssignee() {
            return currentAssignee;
        }

        public String getTaskState() {
            return taskState;
        }

    }
}