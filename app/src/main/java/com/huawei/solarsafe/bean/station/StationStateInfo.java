public class StationStateInfo {
    public static final String TAG = "StationStateInfo";
    private double capacity;
    private double curGeneration;
    private int devAlarm;
    private double equHours;
    private int intelligentAlarm;
    private double perMWPower;
    private double realTimePower;
    private String stationCode;
    private String stationName;
    private String stationStatus;
    public boolean isShowPk,isPkChecked;


    public void setIsShowPk(boolean isShowPk) {
        this.isShowPk = isShowPk;
    }

    public boolean isPkChecked() {
        return isPkChecked;
    }

    public void setIsPkChecked(boolean isPkChecked) {
        this.isPkChecked = isPkChecked;
    }

    public double getCapacity() {
        return capacity;
    }

    public void setCapacity(double capacity) {
        this.capacity = capacity;
    }

    public double getCurGeneration() {
        return curGeneration;
    }

    public void setCurGeneration(double curGeneration) {
        this.curGeneration = curGeneration;
    }

    public int getDevAlarm() {
        return devAlarm;
    }

    public void setDevAlarm(int devAlarm) {
        this.devAlarm = devAlarm;
    }

    public double getEquHours() {
        return equHours;
    }

    public void setEquHours(double equHours) {
        this.equHours = equHours;
    }

    public int getIntelligentAlarm() {
        return intelligentAlarm;
    }

    public void setIntelligentAlarm(int intelligentAlarm) {
        this.intelligentAlarm = intelligentAlarm;
    }

    public double getPerMWPower() {
        return perMWPower;
    }

    public void setPerMWPower(double perMWPower) {
        this.perMWPower = perMWPower;
    }

    public double getRealTimePower() {
        return realTimePower;
    }

    public void setRealTimePower(double realTimePower) {
        this.realTimePower = realTimePower;
    }

    public String getStationCode() {
        return stationCode;
    }

    public void setStationCode(String stationCode) {
        this.stationCode = stationCode;
    }

    public String getStationName() {
        return stationName;
    }

    public void setStationName(String stationName) {
        this.stationName = stationName;
    }

    public String getStationStatus() {
        return stationStatus;
    }

    public void setStationStatus(String stationStatus) {
        this.stationStatus = stationStatus;
    }

    @Override
    public String toString() {
        return "StationStateInfo{" +
                "capacity=" + capacity +
                ", curGeneration=" + curGeneration +
                ", devAlarm=" + devAlarm +
                ", equHours=" + equHours +
                ", intelligentAlarm=" + intelligentAlarm +
                ", perMWPower=" + perMWPower +
                ", realTimePower=" + realTimePower +
                ", stationCode='" + stationCode + '\'' +
                ", stationName='" + stationName + '\'' +
                ", stationStatus='" + stationStatus + '\'' +
                '}';
    }
}
